package com.sist.movie.mongo;

public class FeelVO {
	private String title;
	private String Feel;
	private int count;
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getFeel() {
		return Feel;
	}
	public void setFeel(String Feel) {
		this.Feel = Feel;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
}
